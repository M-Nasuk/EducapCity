  <section id="sec_du_pacte">
    <div>
      <h1 class="jah_font white">Le pacte de fraternité</h1>
    </div>
    <p>EDUCAP CITY est un programme d’éducation populaire, véritable <span class="strong">pacte de fraternité unissant les institutions,
      la société civile et les acteurs économiques</span> et ayant pour ambition <span class="strong">la transmission et
      le partage des valeurs universelles et républicaines</span> auprès de notre jeunesse.
    </p>
    <p>Alors que la défiance de la population grandit envers les institutions et leurs représentants,
      que le harcèlement scolaire est de plus en plus précoce, que se multiplient les manques de civisme,
      les incivilités, le racisme, l'absence d’esprit critique (notamment concernant les « infoxes » véhiculées sans discernement par les réseaux sociaux)
      nous avons mis en place EDUCAP CITY : via ce rallye citoyen, les villes proposent à leurs jeunes la possibilité de sortir des quartiers,
      de se responsabiliser, de réfléchir à leur citoyenneté et à leur place future dans la société.
    </p>
